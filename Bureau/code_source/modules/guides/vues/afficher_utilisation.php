<h2>Guide d'utilisation de l'application</h2>
<br/>
<h3 class="toggle" onclick="$('#connexion').toggle();">Vous connectez ?</h3>
<div id="connexion" class="guide">
	<ul>
		<ol>
			<li>Sur le menu en haut, cliquez sur l'onglet <span class="important">"Connexion"</span>.</li>
			<li>Renseignez votre <span class="important">adresse mail</span>.</li>
			<li>Renseignez votre <span class="important">mot de passe</span>.</li>
			<li>Cliquez sur <span class="important">"Valider"</span> pour vous connecter.</li>
		</ol>
	</ul>
</div>
<br/>

<h3 class="toggle" onclick="$('#getmdp').toggle();">Vous avez oublié votre mot de passe ?</h3>
<div id="getmdp" class="guide">
	<ul>
		<ol>
			<li>Sur le menu en haut, cliquez sur l'onglet <span class="important">"Connexion"</span>.</li>
			<li>Cliquez sur le lien <span class="important">"Mot de passe oublié ?"</span>.</li>
			<li>Renseignez votre <span class="important">adresse mail</span> puis cliquez sur <span class="important">"Valider"</span>.</li>
			<li>Un mail vous sera envoyé contenant un lien pour réinitialiser votre mot de passe.</li>
		</ol>
	</ul>
</div>
<br/>

<h3 class="toggle" onclick="$('#changemdp').toggle();">Vous souhaitez modifier votre mot de passe ?</h3>
<div id="changemdp" class="guide">
	<ul>
		<ol>
			<li>Une fois que vous êtes connectés, cliquez sur l'onglet <span class="important">"Mon compte"</span>.</li>
			<li>Renseignez votre <span class="important">ancien mot de passe</span>.</li>
			<li>Renseignez votre <span class="important">nouveau mot de passe</span> et <span class="important">confirmez le</span>.</li>
			<li>Pour finir, cliquez sur <span class="important">"Modifier mon mot de passe !"</span></li>
		</ol>
	</ul>
</div>
<br/>

<h3 class="toggle" onclick="$('#newticket').toggle();">Vous souhaitez créer un nouveau ticket ?</h3>
<div id="newticket" class="guide">
	<ul>
		<ol>
			<li>Une fois que vous êtes connectés, cliquez sur l'onglet <span class="important">"Création d'un ticket"</span>.</li>
			<li>Donnez un <span class="important">nom</span> à votre ticket.</li>
			<li>Renseignez le <span class="important">problème rencontré</span> dans le champ "Contenu du ticket".</li>
			<li>Sélectionnez une <span class="important">catégorie</span> ainsi qu'une <span class="important">priorité</span> puis cliquez sur <span class="important">"Soumettre ticket"</span>.</li>
		</ol>
	</ul>
</div>
<br/>

<h3 class="toggle" onclick="$('#suivi').toggle();">Vous souhaitez suivre l'avance de vos tickets ?</h3>
<div id="suivi" class="guide">
	<ul>
		<ol>
			<li>Une fois que vous êtes connectés, cliquez sur l'onglet <span class="important">"Suivi des tickets"</span>.</li>
			<li>L'historique de vos tickets apparaît.</li>
			<li>Cliquez sur <span class="important">"Détails"</span> pour faire apparaître les détails du ticket.</li>
		</ol>
	</ul>
</div>
<br/>